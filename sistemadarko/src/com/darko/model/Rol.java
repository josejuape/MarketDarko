package com.darko.model;

public class Rol {
	private int idrol;
	private String desc_rol;
	private String obs_rol;
	
	public int getIdrol() {
		return idrol;
	}
	public void setIdrol(int idrol) {
		this.idrol = idrol;
	}
	public String getDesc_rol() {
		return desc_rol;
	}
	public void setDesc_rol(String desc_rol) {
		this.desc_rol = desc_rol;
	}
	public String getObs_rol() {
		return obs_rol;
	}
	public void setObs_rol(String obs_rol) {
		this.obs_rol = obs_rol;
	}
	
	
}
